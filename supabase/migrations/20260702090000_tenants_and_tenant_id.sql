-- resolve_tenant(host) for the anonymous booking path (D-a30). Definer, read-only.
-- FAILS CLOSED (critic B4): unknown/unmatched host → NULL, never a silent fall-through to 1.
-- book_appointment (093000) rejects a NULL tenant, so a mis-hosted anon call cannot land in Kameraad.
CREATE OR REPLACE FUNCTION public.resolve_tenant(p_host text)
RETURNS int LANGUAGE sql STABLE SECURITY DEFINER SET search_path TO 'public','pg_temp'
AS $$ SELECT id FROM public.tenants WHERE host = p_host AND is_active; $$;   -- NULL if no match
GRANT EXECUTE ON FUNCTION public.resolve_tenant(text) TO anon, authenticated;

-- Add tenant_id to every domain + log table, backfill 1, NOT NULL, indexed.
-- Additive + DEFAULT 1 keeps all live code working (B-01).
DO $$
DECLARE t text;
BEGIN
  FOREACH t IN ARRAY ARRAY['barbers','services','barber_services','availability','blocked_slots',
                           'customers','appointments','email_log','settings','audit_log',
                           'admin_users','content','site_content','marketing_spend'] LOOP
    EXECUTE format('ALTER TABLE public.%I ADD COLUMN IF NOT EXISTS tenant_id int NOT NULL DEFAULT 1 REFERENCES public.tenants(id)', t);
    EXECUTE format('CREATE INDEX IF NOT EXISTS %I ON public.%I (tenant_id)', t||'_tenant_idx', t);
  END LOOP;
END $$;

-- B-03: customers unique per tenant, not globally. The old constraint was raw `email`; the new
-- index is on lower(email), so pre-check for case-variant duplicates BEFORE creating it (a build
-- that already dedupes on the app side; abort + report if any collision remains).
DO $$ DECLARE dupes int; BEGIN
  SELECT count(*) INTO dupes FROM (
    SELECT tenant_id, lower(email) e FROM public.customers GROUP BY 1,2 HAVING count(*) > 1) d;
  IF dupes > 0 THEN RAISE EXCEPTION 'case-variant email dupes present (%): dedupe before the unique index', dupes; END IF;
END $$;
ALTER TABLE public.customers DROP CONSTRAINT IF EXISTS customers_email_key;
CREATE UNIQUE INDEX IF NOT EXISTS customers_tenant_email_key ON public.customers (tenant_id, lower(email));

