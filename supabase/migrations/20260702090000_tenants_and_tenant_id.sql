-- Tenants: the product spine. Kameraad = 1. ICP is config (RQ-K4).
CREATE TABLE IF NOT EXISTS public.tenants (
  id           int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  slug         text UNIQUE NOT NULL,
  name         text NOT NULL,
  host         text UNIQUE,                       -- D-a30 anon booking resolution
  is_active    boolean NOT NULL DEFAULT true,
  -- ICP config surface (RQ-K4): terminology map, thresholds, consent-basis labels,
  -- review platform, brand/voice, locales, sending identity — all data, not code.
  config       jsonb NOT NULL DEFAULT jsonb_build_object(
                 'terminology', jsonb_build_object('provider','barbier','service','dienst'),
                 'thresholds',  jsonb_build_object('rebooking_weeks',5,'lapsed_weeks',8,'dormant_weeks',16,
                                                   'freq_cap_week',1,'freq_cap_month',4),
                 'locales',     jsonb_build_array('nl','en','fr','es','le'),
                 'sender',      jsonb_build_object('tx_domain','mail.kameraadhaarsnijder.be',
                                                   'mkt_domain','news.kameraadhaarsnijder.be')),
  created_at   timestamptz NOT NULL DEFAULT now()
);
